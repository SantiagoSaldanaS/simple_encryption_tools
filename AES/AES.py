# Block Encryption and Decryption (16 bytes)

# Function to encrypt a single 16-byte block following the exact steps of AES.
def encrypt_block(block, expanded_key):

    # We convert the block into a list of integers to manipulate it.
    state = list(block)

    # Initial round: We just add the round key (round 0).
    state = add_round_key(state, expanded_key, 0)

    # We execute 9 full main rounds of the algorithm.
    for round_num in range(1, 10):

        # Step 1: Substitute the bytes.
        state = sub_bytes(state)

        # Step 2: Shift the rows.
        state = shift_rows(state)

        # Step 3: Mix the columns.
        state = mix_columns(state)

        # Step 4: Add the round key for this specific round.
        state = add_round_key(state, expanded_key, round_num)

    # Final round (round 10): We do everything except mixing columns.
    state = sub_bytes(state)
    state = shift_rows(state)
    state = add_round_key(state, expanded_key, 10)

    # We return the fully encrypted state converted back into bytes.
    return bytes(state)


# Function to decrypt a single 16-byte block by doing the AES steps in reverse.
def decrypt_block(block, expanded_key):

    # We convert the block into a list of integers to manipulate it.
    state = list(block)

    # Initial round of decryption starts with the final round key (round 10).
    state = add_round_key(state, expanded_key, 10)

    # We execute 9 full main rounds backwards (from 9 down to 1).
    for round_num in range(9, 0, -1):

        # Step 1: Inverse shift rows.
        state = inv_shift_rows(state)

        # Step 2: Inverse substitute bytes.
        state = inv_sub_bytes(state)

        # Step 3: Add round key.
        state = add_round_key(state, expanded_key, round_num)

        # Step 4: Inverse mix columns.
        state = inv_mix_columns(state)

    # Final decryption round: We undo the first three steps of the original encryption.
    state = inv_shift_rows(state)
    state = inv_sub_bytes(state)
    state = add_round_key(state, expanded_key, 0)

    # We return the fully decrypted state converted back into bytes.
    return bytes(state)


# File Padding

# Function to pad the data so its length is a perfect multiple of 16 (required for AES).
def apply_padding(data):

    # We check how many leftover bytes we have that don't fit perfectly into a 16-byte block.
    remainder = len(data) % 16

    # We calculate how many bytes we need to add to complete the block.
    missing = 16 - remainder

    # We add a single 0x01 byte to mark the beginning of the padding.
    padded_data = data + bytes([0x01])

    # We iterate to fill the rest of the missing space with 0x00 bytes.
    for i in range(missing - 1):
        padded_data = padded_data + bytes([0x00])

    # We return the padded data block.
    return padded_data


# Function to remove the padding after decryption to restore the original file exactly.
def remove_padding(data):

    # We start looking from the very end of the data array.
    position = len(data) - 1

    # We loop backwards, skipping all the 0x00 bytes we added during padding.
    while position >= 0 and data[position] == 0x00:
        position = position - 1

    # Once we stop seeing zeroes, we check if the current byte is the 0x01 marker we placed.
    if position >= 0 and data[position] == 0x01:

        # If it is, we return the data sliced perfectly right before the marker.
        return data[:position]

    # If something went wrong and we don't find the marker, we just return the data as is.
    return data


# ECB Mode Execution

# Function to encrypt an entire dataset using the Electronic Codebook (ECB) mode.
def encrypt_ecb(data, key):

    # We first expand our key to prepare it for encryption.
    expanded_key = expand_key(key)

    # We apply padding to the data so all blocks are exactly 16 bytes.
    data = apply_padding(data)

    # We prepare an empty byte array to store our final ciphered text.
    final_result = bytes()

    # We loop through the entire data array in steps of 16 (block by block).
    for i in range(0, len(data), 16):

        # We extract the current block from the data.
        current_block = data[i : i+16]

        # In ECB mode, we do NOT XOR with a previous block. We just encrypt directly.
        encrypted_block = encrypt_block(current_block, expanded_key)

        # We append the fully encrypted block to our final result.
        final_result = final_result + encrypted_block
        
    # We return the completely encrypted payload.
    return final_result


# Function to decrypt an entire dataset using the Electronic Codebook (ECB) mode.
def decrypt_ecb(data, key):

    # We first expand our key to prepare it for decryption.
    expanded_key = expand_key(key)

    # We prepare an empty byte array to store our restored plaintext.
    final_result = bytes()
    
    # We loop through the entire encrypted data array in steps of 16.
    for i in range(0, len(data), 16):

        # We extract the current ciphered block from the data.
        current_block = data[i : i+16]

        # In ECB mode, we decrypt the block directly without any previous block context.
        decrypted_block = decrypt_block(current_block, expanded_key)

        # We append the decrypted bytes directly to our final plaintext result.
        final_result = final_result + decrypted_block
        
    # We return the final dataset, making sure to remove the padding we added earlier.
    return remove_padding(final_result)


