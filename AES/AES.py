# Key Expansion

# Function to expand the initial 16-byte key into a 176-byte array for the 10 AES rounds.
def expand_key(key):

    # We turn the key into a list so we can freely modify and append new bytes to it.
    exp = list(key)

    # We start with 16 bytes generated since the original key is the first 16 bytes.
    generated_bytes = 16

    # We start tracking at round 1.
    round_num = 1
    
    # We iterate until we have generated all 176 bytes needed for AES-128.
    while generated_bytes < 176:

        # We grab the last 4 bytes generated to use them as a temporary word.
        temp = exp[generated_bytes - 4 : generated_bytes]

        # Every 16 bytes (a full key length), we perform a complex transformation on temp.
        if generated_bytes % 16 == 0:

            # We rotate the array one position to the left.
            rotated = temp[1:] + temp[:1]

            # We reset temp to store the substituted bytes.
            temp = []

            # We pass each rotated byte through the SBOX to add non-linearity.
            for byte in rotated:
                temp.append(SBOX[byte])

            # We XOR the first byte of temp with the Round Constant (RCON) for this specific round.
            temp[0] = temp[0] ^ RCON[round_num]

            # We increment the round counter for the next time we need RCON.
            round_num = round_num + 1
            
        # We iterate 4 times to generate the next 4 bytes of the expanded key.
        for i in range(4):

            # We XOR the byte from 16 positions ago with our temp byte.
            new_byte = exp[generated_bytes - 16] ^ temp[i]

            # We add the newly generated byte to our expanded key list.
            exp.append(new_byte)

            # We increment the generated byte counter.
            generated_bytes = generated_bytes + 1
            
    # Once the loop finishes, we return the full 176-byte expanded key list.
    return exp


# Internal AES Operations

# Function to XOR the current state block with the appropriate section of the expanded key.
def add_round_key(state, expanded_key, round_num):

    # We create an empty list to store the new state block.
    new_state = []

    # We iterate exactly 16 times (the size of an AES block).
    for i in range(16):

        # We calculate the exact starting index in the expanded key based on the current round.
        key_index = (round_num * 16) + i

        # We perform an XOR operation between the state byte and the key byte.
        new_byte = state[i] ^ expanded_key[key_index]

        # We add the new byte to our new state list.
        new_state.append(new_byte)

    # We return the modified state block.
    return new_state


# Function to substitute every byte in the state using the SBOX accordion.
def sub_bytes(state):

    # We create an empty list for the newly substituted bytes.
    new_state = []

    # We iterate through every byte in the current state block.
    for byte in state:

        # We look up the byte in the SBOX and add the result to our list.
        new_state.append(SBOX[byte])

    # We return the substituted state block.
    return new_state


# Function to reverse the byte substitution using the RSBOX accordion (for decryption).
def inv_sub_bytes(state):

    # We create an empty list for the restored bytes.
    new_state = []

    # We iterate through every byte in the current state block.
    for byte in state:

        # We look up the byte in the RSBOX and add the result to our list.
        new_state.append(RSBOX[byte])

    # We return the restored state block.
    return new_state


# Function to shift the rows of the state block to provide diffusion.
def shift_rows(s):

    # We return a manually rearranged list that represents the row shifts.
    # Row 0 is not shifted. Row 1 is shifted left by 1. Row 2 by 2. Row 3 by 3.
    return [s[0], s[5], s[10], s[15],
            s[4], s[9], s[14], s[3],
            s[8], s[13], s[2], s[7],
            s[12], s[1], s[6], s[11]]


# Function to undo the row shifting (for decryption).
def inv_shift_rows(s):

    # We return a manually rearranged list that shifts the rows to the right instead of the left.
    return [s[0], s[13], s[10], s[7],
            s[4], s[1],  s[14], s[11],
            s[8], s[5],  s[2],  s[15],
            s[12], s[9], s[6],  s[3]]


# Function to mix the columns of the state block. 
